    private void btnDesactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesactivarActionPerformed
        int fila = tableDatos.getSelectedRow();
        if (fila != -1) {
            try {
                int modelrow = tableDatos.convertRowIndexToModel(fila);
                AutorizarCorrelativo beanAC = ((TableModelAutorizaCorrelativo) tableDatos.getModel()).getValue(modelrow);
                java.util.Date fechafin = new java.util.Date();
                beanAC.setFechaFin(new java.sql.Date(fechafin.getTime()));
                beanAC.setEstado("D");
                beanAC.setTipoOperacion("A");                                                
                JOptionPane.showMessageDialog(null, (new LogicAutorizaCorrelativo(path)).mantenimiento(beanAC));                
                loadTable();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (InstantiationException ex) {
                Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (IllegalAccessException ex) {
                Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (Exception ex) {
                Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            tableDatos.requestFocus();
        }
    }//GEN-LAST:event_btnDesactivarActionPerformed


