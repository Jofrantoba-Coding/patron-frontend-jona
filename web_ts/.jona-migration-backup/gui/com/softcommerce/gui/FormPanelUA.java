    private void init() throws ClassNotFoundException, InstantiationException, IllegalAccessException, Exception{
        lblNombres.setText(bean.getTrabajador());
        lblUsuario.setText(bean.getNombre());
        modelTable=new TableModelAutorizaCorrelativo();
        tableDatos.setModel(modelTable);
        loadTable();
    }

    public  void loadTable() throws ClassNotFoundException, InstantiationException, IllegalAccessException, Exception{
        modelTable.setData((new LogicAutorizaCorrelativo(path)).listar(bean.getId_usuario()));
        modelTable.fireTableDataChanged();
    }
